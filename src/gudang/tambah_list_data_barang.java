    class tambah_list extends tambah_list_data_barang {
        int id_barang, jumlah_barang;
        String nama_barang, jenis_barang, tanggal_masuk_barang, status;

        public tambah_list() {
            this.nama_barang = text_nama_brg.getText();
            this.jenis_barang = text_jenis_brg.getText();
            this.jumlah_barang =Integer.parseInt(text_jmllh_brg.getText());
            this.status = combo_status_barang.getSelectedItem().toString(); 
            try {
                java.util.Date date = tanggal_masuk.getDate();
                DateFormat dateformat = new SimpleDateFormat("YYYY-MM-dd");
                this.tanggal_masuk_barang = dateformat.format(date);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
     
    }
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Jenis Barang");
        model.addColumn("Jumlah Barang");
        model.addColumn("Tanggal Masuk Barang");
        model.addColumn("Status");
        table_barang.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from list_data");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {                
               Object[] data={
                   rs.getInt("id_barang"),
                   rs.getString("nama_barang"),
                   rs.getString("jenis_barang"),
                   rs.getInt("jumlah_barang"),
                   rs.getDate("tanggal_masuk_barang"),
                   rs.getString("status"),          
               }; 
               model.addRow(data);      
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_id_brg.setText("");
        text_nama_brg.setText("");
        text_jenis_brg.setText("");
        text_jmllh_brg.setText("");
        tanggal_masuk.setDate(null);
        combo_status_barang.setSelectedIndex(0);
    }

