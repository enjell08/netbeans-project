    private void btn_keluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_keluarActionPerformed
        // TODO add your handling code here:
        try {
            this.stat = k.getCon().prepareStatement("delete from list_data where id_barang=?");
            stat.setInt(1, Integer.parseInt(text_id_brg.getText()));
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_keluarActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            tambah_list tl =new tambah_list();
            this.stat = k.getCon().prepareStatement("update list_data set nama_barang=?,"
                    + "jenis_barang=?, jumlah_barang=?, tanggal_masuk_barang=?, status=? where id_barang=?");
            stat.setString(1, tl.nama_barang);
            stat.setString(2, tl.jenis_barang);
            stat.setInt(3, tl.jumlah_barang);
            stat.setString(4, tl.tanggal_masuk_barang);
            stat.setString(5, tl.status);
            stat.setInt(6, Integer.parseInt(text_id_brg.getText()));
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage()); 
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // TODO add your handling code here:
        try {
            tambah_list tl =new tambah_list();
            this.stat = k.getCon().prepareStatement("insert into list_data values(?,?,?,?,?,?)");
            stat.setInt(1, 0);
            stat.setString(2, tl.nama_barang);
            stat.setString(3, tl.jenis_barang);
            stat.setInt(4, tl.jumlah_barang);
            stat.setString(5, tl.tanggal_masuk_barang);
            stat.setString(6, tl.status);
            int pilihan = JOptionPane.showConfirmDialog(null,
                    "Tanggal: "+tl.tanggal_masuk_barang+
                    "\nNama Barang "+tl.nama_barang+
                    "\nJenis Barang"+tl.jenis_barang+
                    "\nJumlah Barang" +tl.jumlah_barang+
                    "\nStatus Barang" +tl.status+"\n",
                    "Tambahkan Data Barang?",
                    JOptionPane.YES_NO_OPTION);
            if (pilihan == JOptionPane.YES_OPTION) {
                this.stat.executeUpdate();
                refreshTable();
            } else if(pilihan == JOptionPane.NO_OPTION) {
                refreshTable();
            } 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());   
        }
    }//GEN-LAST:event_btn_insertActionPerformed

